                <h1>Introduction</h1>

                <p>
                   Le charcutage électoral ou gerrymandering est une pratique qui consiste à modifier les délimitations des circonscriptions électorales pour en attirer un avantage politique, en favorisant un parti politique. Cette pratique, bien qu'illégale, est présente dans les processus électoraux grâce aux progrès informatiques qui permettent de dissimuler des découpages déloyaux, et à la faiblesse des outils juridiques destinés au contrôle et la surveillance de la légalité des redécoupages électoraux
                </p>
                <div class="photo1">
                    <img src="Pics/Pic1.png"  class="float"/>

                    <p class="par1">Le terme <i>Gerrymandering</i>, contraction de <i>Gerry</i> et <i>salamander</i>, apparaît dans la littérature anglaise en 1812 dans une publication du journal <i>The Boston Gazette</i> pour désigner le découpage partisan fait par le gouverneur de l'État de Massachusetts, Elbridge Gerry, dont la forme d'une des circonscriptions redécoupées de la ville de Boston ressemblait à un <i>Salamandre</i>. Ce jeu de mots deviendra désormais la manière de qualifier les méthodes de redécoupage visant à favoriser ou défavoriser un parti politique.
                    </p> 
                </div>
                <br>
                <br>
                <br>
                <br>

                <p>
                    Généralement nous retrouvons le gerrymandering sous l'une des formes suivantes:
                </p>
                <ul>
                    <p>
                        <strong>•   Gerrymandering partisan :</strong> consiste à redessiner les délimitations des circonscriptions électorales afin de bénéficier du pouvoir politique, limiter l'influence des groupes politiques de l'opposition, et de se maintenir au pouvoir.
                    </p>

                    <p>
                        <strong>•   Gerrymandering racial :</strong> c'est l'ensemble des modifications dans les limites  des circonscriptions ayant pour objectif la dilution du vote d'un  ou de plusieurs groupes ethniques. Il s'agit d'un moyen pour étouffer la voix de ces minorités.
                    </p>

                   <p> 
                        <strong>•   Incumbent gerrymandering :</strong> l'objectif du redécoupage est de perpétuer l'influence des <i>incumbents<sup>1</sup></i> en leur assurant un siège institutions qui exercent le pouvoir législatif
                    </p>
                </ul>

                <br>


                <p>
                    <sup>1</sup>Mot pour désigner les membres élus (maires, représentants, sénateurs, etc.) qui sont en exercice lors des élections.
                    
                </p>
                <p>
                    Pour réaliser un gerrymander, les politiciens utilisent les techniques suivantes :
                    
                </p>
                <ul>
                    <p>
                        <strong>•    Packing:</strong> consiste à modifier les circonscriptions de façon à concentrer le vote de l'adversaire politique dans une zone déterminée, et réduire leur impact sur l'ensemble du territoire.
                    </p>

                    <p>
                        <strong>•   Cracking:</strong> consiste à modifier les circonscriptions de façon à réduire l'impact de l'adversaire, en dispersant leur vote. 
                        
                    </p>

                    <p>
                        <strong>•   Stacking:</strong> consiste à redessiner les circonscriptions de façon à concentrer un groupe de votants dans une région déterminée. Cependant, l'objectif de ces actions n'étant pas de diluer leur influence, mais plutôt de l'augmenter pour ainsi obtenir un maximum de sièges dans un territoire “hostile”.
                        
                    </p>
                    
                </ul>               
                

